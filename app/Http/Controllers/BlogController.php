    {
        $id = $request->get('id', '');
        // $blog = Blog::findOrFail($id);
        $blog = Blog::query()
            ->where('id', $id)->first();

        if (empty($blog)) {
            return response()->json(['message' => 'Không tìm thấy sản phẩm']);
        }
        return response()->json(['data' => $blog]);
    }

